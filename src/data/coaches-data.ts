export function getCoachByTeam(
  teamSlug: string
): CoachProfile | undefined {
  const coach = COACHES.find((c) => c.teamSlug === teamSlug)
  if (coach && !coach.photo && COACH_PHOTOS[teamSlug]) {
    coach.photo = COACH_PHOTOS[teamSlug]
  }
  return coach
}
